public class Calculator {
    public int adunare(int a, int b)
    {
        return a+b;
    }
    public float adunare(float a, float b)
    {
        return a+b;
    }
    public float adunare(float a, float b, float c)
    {
        return a+b+c;
    }
    public double adunare(int a, int b, int c, int d)
    {
        return a+b+c+d;
    }

    public int scadere(int a, int b)
    {
        return a-b;
    }
    public float scadere(float a, int b)
    {
        return (float)a-b;
    }
    public int inmultire(int a, int b)
    {
        return a*b;
    }
    public float inmultire(float a, float b, float  c)
    {
        return a*b*c;
    }
    public float impartire(float a, float b)
    {
        return a/b;
    }
    public float impartire(int a, int b)
    {
        return a/b;
    }
}